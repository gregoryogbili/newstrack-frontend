function extractDomain(url = "") {
  try {
    const u = new URL(url);
    return u.hostname.replace("www.", "");
  } catch {
    return null;
  }
}

export function getOwnershipInfo(sourceName, sourceUrl = null) {
  // 1️⃣ Direct name match
  if (sourceName && MEDIA_OWNERSHIP[sourceName]) {
    return MEDIA_OWNERSHIP[sourceName];
  }

  // 2️⃣ Partial name match — catches "BBC World", "Reuters Business", "CNN Top Stories" etc.
  if (sourceName) {
    const normalized = Object.keys(MEDIA_OWNERSHIP).find((key) =>
      sourceName.toLowerCase().includes(key.toLowerCase()),
    );
    if (normalized) return MEDIA_OWNERSHIP[normalized];
  }

  // 3️⃣ URL substring fast-path
  if (sourceUrl) {
    const url = sourceUrl.toLowerCase();
    if (url.includes("bbc.co") || url.includes("bbc.com"))
      return MEDIA_OWNERSHIP["BBC"];
    if (url.includes("reuters.com") || url.includes("reutersagency.com"))
      return MEDIA_OWNERSHIP["Reuters"];
    if (url.includes("cnn.com")) return MEDIA_OWNERSHIP["CNN"];
    if (url.includes("apnews.com")) return MEDIA_OWNERSHIP["Associated Press"];
    if (url.includes("theguardian.com")) return MEDIA_OWNERSHIP["Guardian"];
    if (url.includes("aljazeera.com")) return MEDIA_OWNERSHIP["Al Jazeera"];
    if (url.includes("bloomberg.com")) return MEDIA_OWNERSHIP["Bloomberg"];
    if (url.includes("ft.com")) return MEDIA_OWNERSHIP["Financial Times"];
    if (url.includes("nytimes.com")) return MEDIA_OWNERSHIP["New York Times"];
  }

  // 4️⃣ Domain lookup
  if (sourceUrl) {
    const domain = extractDomain(sourceUrl);
    if (domain) {
      for (const key in DOMAIN_OWNERS) {
        if (domain.includes(key)) return DOMAIN_OWNERS[key];
      }
    }
  }

  return null;
}

